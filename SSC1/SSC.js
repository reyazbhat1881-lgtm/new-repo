        let currentTest = null;
        let timeLeft = 0;
        let timerInterval = null;
        let startTime = null;
        let totalChars = 0;
        let correctChars = 0;
        let errorCount = 0;

        function renderTests() {
            const grid = document.getElementById('testsGrid');
            grid.innerHTML = tests.map(test => `
                <div class="test-card">
                    <h3>${test.title}</h3>
                    <div class="test-info">
                        <div class="test-info-item">
                            <span>Duration:</span>
                            <span><strong>${test.duration} Minutes</strong></span>
                        </div>
                        <div class="test-info-item">
                            <span>Type:</span>
                            <span><strong>Practice Test</strong></span>
                        </div>
                    </div>
                    <select class="language-select">
                        <option value="english">English</option>
                        <option value="hindi">Hindi (Coming Soon)</option>
                    </select>
                    <button class="start-btn" onclick="startTest(${test.id})">Start Typing Test â†’</button>
                </div>
            `).join('');
        }

        function startTest(testId) {
            currentTest = tests.find(t => t.id === testId);
            if (!currentTest) return;

            document.getElementById('homePage').style.display = 'none';
            document.getElementById('typingPage').style.display = 'block';
            document.getElementById('testTitle').textContent = currentTest.title;
            
            timeLeft = currentTest.duration * 60;
            totalChars = 0;
            correctChars = 0;
            errorCount = 0;
            
            displayText();
            updateTimer();
            updateStats();
            
            const input = document.getElementById('typingInput');
            input.value = '';
            input.focus();
            
            if (timerInterval) clearInterval(timerInterval);
            startTime = Date.now();
            timerInterval = setInterval(updateTimer, 1000);
        }

        function displayText() {
            const display = document.getElementById('textDisplay');
            display.innerHTML = currentTest.text.split('').map((char, i) => 
                `<span id="char-${i}">${char}</span>`
            ).join('');
        }

        function updateTimer() {
            const minutes = Math.floor(timeLeft / 60);
            const seconds = timeLeft % 60;
            document.getElementById('timer').textContent = 
                `${minutes.toString().padStart(2, '0')}:${seconds.toString().padStart(2, '0')}`;
            
            if (timeLeft <= 0) {
                clearInterval(timerInterval);
                alert('Time is up! Test completed.');
                return;
            }
            timeLeft--;
        }

        function updateStats() {
            const elapsedMinutes = (Date.now() - startTime) / 60000;
            const wpm = elapsedMinutes > 0 ? Math.round((correctChars / 5) / elapsedMinutes) : 0;
            const accuracy = totalChars > 0 ? Math.round((correctChars / totalChars) * 100) : 100;
            
            document.getElementById('wpm').textContent = wpm;
            document.getElementById('accuracy').textContent = accuracy + '%';
            document.getElementById('chars').textContent = totalChars;
            document.getElementById('errors').textContent = errorCount;
        }

        document.getElementById('typingInput').addEventListener('input', function(e) {
            if (!startTime) startTime = Date.now();
            
            const typed = e.target.value;
            const original = currentTest.text;
            
            totalChars = typed.length;
            correctChars = 0;
            errorCount = 0;
            
            for (let i = 0; i < original.length; i++) {
                const charElement = document.getElementById(`char-${i}`);
                if (!charElement) continue;
                
                charElement.classList.remove('correct', 'incorrect', 'current');
                
                if (i < typed.length) {
                    if (typed[i] === original[i]) {
                        charElement.classList.add('correct');
                        correctChars++;
                    } else {
                        charElement.classList.add('incorrect');
                        errorCount++;
                    }
                } else if (i === typed.length) {
                    charElement.classList.add('current');
                }
            }
            
            updateStats();
            
            if (typed === original) {
                clearInterval(timerInterval);
                alert('Congratulations! You completed the test!');
            }
        });

        function resetTest() {
            if (currentTest) {
                startTest(currentTest.id);
            }
        }

        function goBack() {
            if (timerInterval) clearInterval(timerInterval);
            document.getElementById('typingPage').style.display = 'none';
            document.getElementById('homePage').style.display = 'block';
            currentTest = null;
        }

        function showPage(pageName) {
            const pages = ['homePage', 'aboutPage', 'contactPage', 'privacyPage', 'termsPage', 'typingPage'];
            pages.forEach(page => {
                document.getElementById(page).style.display = 'none';
            });
            document.getElementById(pageName).style.display = 'block';
            
            document.querySelectorAll('.nav-link').forEach(link => {
                link.classList.remove('active');
            });
            document.querySelector(`[data-page="${pageName.replace('Page', '')}"]`)?.classList.add('active');
            
            window.scrollTo(0, 0);
        }

        document.querySelectorAll('.nav-link, .footer-links a').forEach(link => {
            link.addEventListener('click', function(e) {
                e.preventDefault();
                const page = this.getAttribute('data-page');
                showPage(page + 'Page');
            });
        });

